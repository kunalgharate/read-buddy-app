import 'dart:io';

import 'package:read_buddy_app/features/bookcrud/domain/entities/book_crud.dart';

class BookCrudModel extends BookCrudEntity {
  final List<String> additionalImageUrls;
  final File? coversingleImage;

  BookCrudModel({
    required String title,
    String? subtitle,
    required String author,
    required String publisher,
    required int publicationYear,
    required String isbn,
    required String edition,
    required String condition,
    required bool isAvailable,
    required String status,
    required int numberOfCopies,
    required String format,
    required String language,
    required String genre,
    required List<String> tags,
    required String category,
    String? categoryId,
    String? id,
    required String ownerId,
    String? ownerName,
    required String location,
    this.coversingleImage,
    required String coverImageUrl,
    required List<File> additionalImages,
    List<String>? additionalImageUrls,
    required String description,
    required String notes,
  })  : additionalImageUrls = additionalImageUrls ?? [],
        super(
          title: title,
          subtitle: subtitle ?? "subtitle",
          author: author,
          publisher: publisher,
          publicationYear: publicationYear,
          isbn: isbn,
          edition: edition,
          condition: condition,
          isAvailable: isAvailable,
          status: status,
          numberOfCopies: numberOfCopies,
          format: format,
          language: language,
          genre: genre,
          tags: tags,
          category: category,
          categoryId: categoryId,
          id: id ?? "id-",
          ownerId: ownerId,
          ownerName: ownerName,
          location: location,
          coverImageUrl: coverImageUrl,
          additionalImages: additionalImages,
          description: description,
          notes: notes,
          coversingleImage: coversingleImage,
        );

  factory BookCrudModel.fromJson(Map<String, dynamic> json) {
    return BookCrudModel(
      title: json['title'] ?? "title",
      subtitle: json['subtitle'] ?? "subtitle",
      author: json['author'] ?? "-author-",
      publisher: json['publisher'] ?? "_pub-",
      publicationYear: json['publication_year'] ?? 0,
      isbn: json['isbn'] ?? "isbn",
      edition: json['edition'] ?? "edition",
      condition: json['condition'] ?? "-condition",
      isAvailable: json['is_available'] ?? false,
      status: json['status'] ?? "stat",
      numberOfCopies: json['number_of_copies'] ?? 0,
      format: json['format'] ?? "format",
      language: json['language'] ?? "language",
      genre: json['genre'] ?? "genre",
      tags: List<String>.from(json['tags'] ?? []),
      category: json['category']?['name'] ?? "category",
      categoryId: json['category']?['_id'],
      id: json['_id'],
      ownerId: json['ownerId']?['_id'] ?? "",
      ownerName: json['ownerId']?['name'],
      location: json['location'] ?? "location",
      coverImageUrl: json['coverImageUrl'] ?? "",
      additionalImageUrls: List<String>.from(json['additional_images'] ?? []),
      additionalImages: [], // Cannot get File from API
      description: json['description'] ?? "",
      notes: json['notes'] ?? "",
      coversingleImage: null,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'title': title,
      'subtitle': subtitle,
      'author': author,
      'publisher': publisher,
      'publication_year': publicationYear,
      'isbn': isbn,
      'edition': edition,
      'condition': condition,
      'is_available': isAvailable,
      'status': status,
      'number_of_copies': numberOfCopies,
      'format': format,
      'language': language,
      'genre': genre,
      'tags': tags,
      'category': category,
      'categoryId': categoryId,
      'ownerId': ownerId,
      'location': location,
      'coverImageUrl': coverImageUrl,
      'additional_images': additionalImageUrls,
      'description': description,
      'notes': notes,
      // 'coversingleImage': Not added because File can't be serialized to JSON directly
    };
  }

  factory BookCrudModel.fromEntity(BookCrudEntity book) {
    return BookCrudModel(
      id: book.id,
      title: book.title,
      subtitle: book.subtitle,
      author: book.author,
      publisher: book.publisher,
      publicationYear: book.publicationYear,
      isbn: book.isbn,
      edition: book.edition,
      condition: book.condition,
      isAvailable: book.isAvailable,
      status: book.status,
      numberOfCopies: book.numberOfCopies,
      format: book.format,
      language: book.language,
      genre: book.genre,
      tags: book.tags,
      category: book.category,
      categoryId: book.categoryId,
      ownerId: book.ownerId,
      ownerName: book.ownerName,
      location: book.location,
      coverImageUrl: book.coverImageUrl,
      additionalImages: book.additionalImages,
      description: book.description,
      notes: book.notes,
      coversingleImage: book.coversingleImage,
    );
  }

  BookCrudEntity toEntity() => BookCrudEntity(
        id: id,
        title: title,
        subtitle: subtitle,
        author: author,
        publisher: publisher,
        publicationYear: publicationYear,
        isbn: isbn,
        edition: edition,
        condition: condition,
        isAvailable: isAvailable,
        status: status,
        numberOfCopies: numberOfCopies,
        format: format,
        language: language,
        genre: genre,
        tags: tags,
        category: category,
        categoryId: categoryId,
        ownerId: ownerId,
        ownerName: ownerName,
        location: location,
        coverImageUrl: coverImageUrl,
        additionalImages: additionalImages,
        description: description,
        notes: notes,
        coversingleImage: coversingleImage,
      );

  BookCrudModel copyWith({
    String? condition,
    String? ownerId,
    String? location,
    String? description,
    String? coverImageUrl,
    File? coversingleImage,
    List<File>? additionalImages,
    List<String>? additionalImageUrls,
    List<String>? tags,
    String? notes,
  }) {
    return BookCrudModel(
      id: id,
      title: title,
      subtitle: subtitle,
      author: author,
      publisher: publisher,
      publicationYear: publicationYear,
      isbn: isbn,
      edition: edition,
      condition: condition ?? this.condition,
      isAvailable: isAvailable,
      status: status,
      numberOfCopies: numberOfCopies,
      format: format,
      language: language,
      genre: genre,
      tags: tags ?? this.tags,
      category: category,
      categoryId: categoryId,
      ownerId: ownerId ?? this.ownerId,
      ownerName: ownerName,
      location: location ?? this.location,
      coversingleImage: coversingleImage ?? this.coversingleImage,
      coverImageUrl: coverImageUrl ?? this.coverImageUrl,
      additionalImages: additionalImages ?? this.additionalImages,
      additionalImageUrls: additionalImageUrls ?? this.additionalImageUrls,
      description: description ?? this.description,
      notes: notes ?? this.notes,
    );
  }
}
